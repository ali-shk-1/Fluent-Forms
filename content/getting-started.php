<h1>Getting Started</h1>
<p>Welcome to <a href="#">Fluent Forms</a> documentation.</p>
<p>Fluent Forms is a powerful, user-oriented WordPress plugin that allows you to create amazing forms without requiring any coding skills.</p>
<p>With its simple drag-and-drop interface, you can quickly create forms that look and function exactly as you want. Whether you need a simple contact form or a complex <a href="#">multi-step form</a>, <strong>Fluent Forms</strong> has you covered.</p>
<h2>Key Features</h2>
<ul>
    <li>Drag and drop form builder</li>
    <li>Advanced field option</li>
    <li><a href="#">Conditional logic</a></li>
    <li>Integration with popular CRM tools</li>
    <li>Advanced Customization options</li>
</ul>