<h1>Install Fluent Forms</h1>
<p>Before installing, make sure your site meets these requirements:</p>
<ul>
    <li>WordPress version 4.5 or later</li>
    <li>PHP version 5.4 or later</li>
</ul>

<h2>Install from WordPress dashboard</h2>
<p>Navigate to Plugins → Add New, search for Fluent Forms, click Install Now, then Activate.</p>
<img src="https://docs.fluentforms.com/images/getting-started/install-fluent-forms/Fluent-Form-install-01-scaled.webp" alt="Installing Fluent Forms from WordPress" class="docs-img">

<h2>Install from WordPress.org</h2>
<p>Download the plugin zip from WordPress.org, then upload it manually via Plugins → Add New → Upload Plugin.</p>
<img src="https://docs.fluentforms.com/images/getting-started/install-fluent-forms/02-2-scaled.webp" alt="Installing from WordPress Repository" class="docs-img">
<img src="https://docs.fluentforms.com/images/getting-started/install-fluent-forms/03-3-scaled.webp" alt="Add New Plugin" class="docs-img">
<img src="https://docs.fluentforms.com/images/getting-started/install-fluent-forms/04-2-scaled.webp" alt="Install Now" class="docs-img">
<img src="https://docs.fluentforms.com/images/getting-started/install-fluent-forms/05-scaled.webp" alt="Activate" class="docs-img">

<p>Once installed, click Activate. You're now ready to create your first form.</p>