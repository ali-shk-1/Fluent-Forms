<h1>Glossary</h1>
<p>Quick reference for common terms used across Fluent Forms documentation.</p>
<ul>
    <li><strong>Field</strong> — a single input element in a form (text, dropdown, checkbox, etc).</li>
    <li><strong>Entry</strong> — a single form submission stored in the database.</li>
    <li><strong>Notification</strong> — an automated email sent after a form is submitted.</li>
    <li><strong>Confirmation</strong> — the message or redirect shown to the user after submitting.</li>
    <li><strong>Integration</strong> — a connection between Fluent Forms and a third-party service (Mailchimp, Slack, etc).</li>
    <li><strong>Shortcode</strong> — a code snippet used to embed a form on any page or post.</li>
</ul>